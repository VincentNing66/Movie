DROP SCHEMA IF EXISTS magicmovie;
CREATE SCHEMA magicmovie;
USE magicmovie;
SET GLOBAL sql_mode=(SELECT REPLACE(@@sql_mode,'ONLY_FULL_GROUP_BY',''));

DROP TABLE IF exists User;
CREATE TABLE User (
    UserID INT AUTO_INCREMENT PRIMARY KEY,
    Username VARCHAR(255) NOT NULL UNIQUE,
    Password VARCHAR(255) NOT NULL,
    UserType ENUM('Customer', 'Staff', 'Manager','Admin')
);

DROP TABLE IF exists Customer;
CREATE TABLE Customer (
    Customerid INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT UNIQUE,
    First_name VARCHAR(100) NOT NULL,
    Last_name VARCHAR(100) NOT NULL,
    Con VARCHAR(20),
    Birthdate DATE,
    FOREIGN KEY (UserID) REFERENCES User(UserID)
);

DROP TABLE IF exists Staff;
CREATE TABLE Staff (
    StaffID INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT UNIQUE,
	First_name VARCHAR(100) NOT NULL,
    Last_name VARCHAR(100) NOT NULL,
    Con VARCHAR(20),
    Department VARCHAR(255),
    FOREIGN KEY (UserID) REFERENCES User(UserID)
);

DROP TABLE IF exists Manager;
CREATE TABLE Manager (
    Managerid INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT UNIQUE,
    First_name VARCHAR(100) NOT NULL,
    Last_name VARCHAR(100) NOT NULL,
    Con VARCHAR(20),
    Department VARCHAR(255),
    FOREIGN KEY (UserID) REFERENCES User(UserID)
);

DROP TABLE IF exists Admin;
CREATE TABLE Admin (
    Adminid INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT,
    First_name VARCHAR(100) NOT NULL,
    Last_name VARCHAR(100) NOT NULL,
    Con VARCHAR(20),
    Department VARCHAR(255),
    FOREIGN KEY (UserID) REFERENCES User(UserID)
);

DROP TABLE IF exists Movies;
CREATE TABLE Movies (
    MovieID INT AUTO_INCREMENT PRIMARY KEY,
    Title VARCHAR(255) NOT NULL,
    Genre VARCHAR(100),
	Director VARCHAR(255),
    Duration INT,
    ReleaseDate DATE,
    Rating VARCHAR(50),
    Detail VARCHAR(255),
    BasePrice DECIMAL(6,2)
);

DROP TABLE IF exists MoviesImages;
CREATE TABLE MoviesImages (
    ImageID INT AUTO_INCREMENT PRIMARY KEY,
    MovieID INT,
    image_path VARCHAR(255),
    FOREIGN KEY (MovieID) REFERENCES Movies(MovieID)
    );

DROP TABLE IF exists CINEMA;
CREATE TABLE CINEMA (
CinemaID INT AUTO_INCREMENT PRIMARY KEY,
CinemaName VARCHAR(100),
Capacity INT,
Type VARCHAR(100));

DROP TABLE IF exists CINEMAImages;
CREATE TABLE CINEMAImages (
    ImageID INT AUTO_INCREMENT PRIMARY KEY,
    CinemaID INT,
    image_path VARCHAR(255),
    FOREIGN KEY (CinemaID) REFERENCES CINEMA(CinemaID)
    );

DROP TABLE IF exists Session;
CREATE TABLE Session (
    SessionID INT AUTO_INCREMENT PRIMARY KEY,
    MovieID INT,
    CinemaID INT,
	SessionDateTime DATETIME,
    SeatAvailability INT,
    FOREIGN KEY (MovieID) REFERENCES Movies(MovieID),
    FOREIGN KEY (CinemaID) REFERENCES CINEMA(CinemaID)
);
DROP TABLE IF exists Seats;
CREATE TABLE Seats (
    SeatID INT AUTO_INCREMENT PRIMARY KEY,
    SessionID INT,
    SeatNumber VARCHAR(10),
    IsAvailable BOOLEAN DEFAULT TRUE,
    FOREIGN KEY (SessionID) REFERENCES Session(SessionID)
);
-- 票价表
DROP TABLE IF exists TicketPrices;
CREATE TABLE TicketPrices (
    PriceID INT PRIMARY KEY AUTO_INCREMENT,
    Type VARCHAR(50),
    DiscountAmount  DECIMAL(5, 2)
);

-- 折扣表

DROP TABLE IF EXISTS Discounts;
CREATE TABLE Discounts (
    DiscountID INT PRIMARY KEY AUTO_INCREMENT,
    Description VARCHAR(255),
    DiscountPercent DECIMAL(5, 2) DEFAULT 0,
    StartDate DATE,
    EndDate DATE,
    WeekDays VARCHAR(7),  -- '1234567' 表示周一到周日
    SpecificDates TEXT,  -- 特定日期，例如 '2023-12-25,2023-01-01'
    StartTime TIME DEFAULT '00:00:00',  -- 默认全天开始
    EndTime TIME DEFAULT '23:59:59',    -- 默认全天结束
    image_path VARCHAR(255)
);



-- 场次和折扣关联表
DROP TABLE IF exists SessionDiscounts;
CREATE TABLE SessionDiscounts (
    SessionID INT,
    DiscountID INT,
    UNIQUE(SessionID, DiscountID),
    FOREIGN KEY (SessionID) REFERENCES Session(SessionID),
    FOREIGN KEY (DiscountID) REFERENCES Discounts(DiscountID)
);

DROP TABLE IF exists Orders;
CREATE TABLE Orders (
    OrderID INT AUTO_INCREMENT PRIMARY KEY,
    SessionID INT NOT NULL,
    UserID INT NOT NULL,
    TotalPrice DECIMAL(10, 2) NOT NULL,
    CreatedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (SessionID) REFERENCES Session(SessionID),
    FOREIGN KEY (UserID) REFERENCES User(UserID)
);

DROP TABLE IF exists OrderDetails;
CREATE TABLE OrderDetails (
    OrderDetailID INT AUTO_INCREMENT PRIMARY KEY,
    OrderID INT NOT NULL,
    SeatNumber VARCHAR(10),
    TicketType VARCHAR(50),
    TicketPrice DECIMAL(6,2),
    FOREIGN KEY (OrderID) REFERENCES Orders(OrderID)

);

DROP TABLE IF exists Bookings;
CREATE TABLE Bookings (
	BookingID INT PRIMARY KEY AUTO_INCREMENT,
	CustomerID INT ,
    UserID INT,
    SessionID INT,
    TotalPrice DECIMAL(6,2),
    PaymentStatus ENUM('Pending', 'Completed'),
    FOREIGN KEY (UserID) REFERENCES User(UserID),
    FOREIGN KEY (CustomerID) REFERENCES Customer (Customerid),
    FOREIGN KEY (SessionID) REFERENCES Session(SessionID)
);


DROP TABLE IF exists Tickets;
CREATE TABLE Tickets (
    TicketID INT AUTO_INCREMENT PRIMARY KEY,
    BookingID INT,
    UserID  INT,
    MovieID INT,
    SessionID INT,
    SeatNumber VARCHAR(10),
    FOREIGN KEY (BookingID) REFERENCES Bookings(BookingID),
    FOREIGN KEY (MovieID) REFERENCES Movies(MovieID),
    FOREIGN KEY (SessionID) REFERENCES Session(SessionID)
);
