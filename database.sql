    BookingDetailID INT AUTO_INCREMENT PRIMARY KEY,
    BookingID INT NOT NULL,
    SeatNumber VARCHAR(10),
    UnitPrice DECIMAL(6,2),
    Type VARCHAR(50),
    TicketID INT,
    FOREIGN KEY (BookingID) REFERENCES Bookings(BookingID),
